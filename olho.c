void draw_centered(const char *frame) {
    
    int rows, cols;
    getmaxyx(stdscr, rows, cols);

    int frame_h = 0;

    for (const char *p = frame; *p; p++) {
        if (*p == '\n') frame_h++;
    }

    int start_y = (rows - frame_h) / 2;
    if (start_y < 0) start_y = 0;

    int start_x = cols / 4;

    int y = start_y;

    const char *p = frame;

    while (*p) {
        const char *line_start = p;

        while (*p && *p != '\n') p++;

        int len = p - line_start;

        mvprintw(y++, start_x, "%.*s", len, line_start);

        if (*p == '\n') p++;
    }
}

