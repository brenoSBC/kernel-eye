#include <ncurses.h>

#include <stdio.h>
#include <inttypes.h>

#include "display.h"

void ncurses_init(void) {
    initscr();
    noecho();
    cbreak();
    start_color();
    curs_set(0);

    init_pair(1, COLOR_WHITE, COLOR_BLACK);
    init_pair(2, COLOR_CYAN, COLOR_BLACK);
    init_pair(3, COLOR_YELLOW, COLOR_BLACK);
}

void draw_header(WINDOW *window) {
    
    werase(window);
    box(window, 0, 0); 

   
    wrefresh(window);
}

void draw_cpu(WINDOW *window, System *sys) {

    werase(window);
    box(window, 0, 0);

    wrefresh(window);

}

